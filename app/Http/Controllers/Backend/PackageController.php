     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|string|max:255',
        ], [
        
            'name.required' => 'Không được bỏ trống tên gói',

        ]);
        $package = new Package();
        $package->name = $request->input('name');
        $package->save();
        return redirect()->route('package.index');

